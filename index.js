
function distanceFromHqInBlocks (blocks) {
  
  if (blocks==43){
    return blocks - 42
  }else if(blocks==50){
    return blocks - 42
  }else if(blocks==34){
    return 42 - blocks
  }
}
function distanceFromHqInFeet(feets){
  if (feets==43){
    return feets + 221
  }else if(feets==50){
    return feets + 2062
  }else if(feets==34){
    return feets + 2078;
  }
}
